#!/usr/bin/env node
/**
 * Pre-render enrolment_confirmation email HTML in Node (React Email works here).
 * Edge Functions substitute runtime values — no React in Deno.
 */
import { mkdirSync, writeFileSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath, pathToFileURL } from 'node:url';

const root = join(dirname(fileURLToPath(import.meta.url)), '..');
const outDir = join(root, 'supabase/functions/_shared/enrolment-confirmation-email-shells');
const outFile = join(outDir, 'generated.ts');

const PLACEHOLDER = {
  schoolName: '{{SCHOOL_NAME}}',
  recipientName: '{{RECIPIENT_NAME}}',
  studentName: '{{STUDENT_NAME}}',
  className: '{{CLASS_NAME}}',
  classDay: '{{CLASS_DAY}}',
  classTime: '{{CLASS_TIME}}',
  classStartDate: '{{CLASS_START_DATE}}',
  classTeacher: '{{CLASS_TEACHER}}',
  location: '{{LOCATION}}',
  amountPaid: '{{AMOUNT_PAID}}',
  paidOn: '{{PAID_ON}}',
  paymentMethod: '{{PAYMENT_METHOD}}',
  signUrl: '{{SIGN_URL}}',
  deadlineDate: '{{DEADLINE_DATE}}',
  headline: '{{HEADLINE}}',
  primary: '{{PRIMARY_COLOR}}',
  accent: '{{ACCENT_COLOR}}',
};

const BUILD_PRIMARY = '#a1b2c3';
const BUILD_ACCENT = '#d4e5f6';
const BUILD_DEADLINE = 'Monday, 1 January 2027';
const BUILD_AMOUNT = '₪350.00';
const BUILD_PAID_ON = 'Monday, 23 June 2026';
const BUILD_PAYMENT_METHOD = 'Visa ending in 4242';

const { EMAIL_TEMPLATE_NAMES, renderEmailTemplate } = await import(
  pathToFileURL(join(root, 'packages/shared/dist/email/render-template.js')).href,
);

async function renderShell(language, pendingWaiver) {
  const { html } = await renderEmailTemplate({
    templateName: EMAIL_TEMPLATE_NAMES.ENROLMENT_CONFIRMATION,
    language,
    schoolName: PLACEHOLDER.schoolName,
    tenantColors: {
      primary_color: BUILD_PRIMARY,
      accent_color: BUILD_ACCENT,
    },
    variables: {
      recipientName: PLACEHOLDER.recipientName,
      studentName: PLACEHOLDER.studentName,
      showStudentRow: true,
      headline: PLACEHOLDER.headline,
      className: PLACEHOLDER.className,
      classDetails: {
        day: PLACEHOLDER.classDay,
        time: PLACEHOLDER.classTime,
        startDate: PLACEHOLDER.classStartDate,
        teacher: PLACEHOLDER.classTeacher,
      },
      location: PLACEHOLDER.location,
      paymentSummary: {
        amountFormatted: BUILD_AMOUNT,
        paidOnFormatted: BUILD_PAID_ON,
        paymentMethodLabel: BUILD_PAYMENT_METHOD,
      },
      pendingWaiver,
      signUrl: pendingWaiver ? PLACEHOLDER.signUrl : undefined,
      deadlineDate: pendingWaiver ? BUILD_DEADLINE : undefined,
    },
  });

  return html
    .split(BUILD_PRIMARY).join(PLACEHOLDER.primary)
    .split(BUILD_ACCENT).join(PLACEHOLDER.accent)
    .split(BUILD_DEADLINE).join(PLACEHOLDER.deadlineDate)
    .split(BUILD_AMOUNT).join(PLACEHOLDER.amountPaid)
    .split(BUILD_PAID_ON).join(PLACEHOLDER.paidOn)
    .split(BUILD_PAYMENT_METHOD).join(PLACEHOLDER.paymentMethod)
    .split('var(--email-primary)').join(PLACEHOLDER.primary)
    .split('var(--email-accent)').join(PLACEHOLDER.accent);
}

const shells = {
  en: {
    confirmed: await renderShell('en', false),
    pending_waiver: await renderShell('en', true),
  },
  he: {
    confirmed: await renderShell('he', false),
    pending_waiver: await renderShell('he', true),
  },
};

mkdirSync(outDir, { recursive: true });

const ts = `/** Generated by scripts/build-enrolment-confirmation-email-shells.mjs — do not edit. */
export type EnrolmentConfirmationLanguage = 'en' | 'he';
export type EnrolmentConfirmationVariant = 'confirmed' | 'pending_waiver';

const PLACEHOLDER = ${JSON.stringify(PLACEHOLDER)} as const;

export const ENROLMENT_CONFIRMATION_SHELLS: Record<
  EnrolmentConfirmationLanguage,
  Record<EnrolmentConfirmationVariant, string>
> = ${JSON.stringify(shells, null, 2)} as const;

function escapeHtml(value: string): string {
  return value
    .replace(/&/g, '&amp;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;')
    .replace(/"/g, '&quot;');
}

/** Remove optional detail rows marked with data-ms-opt-row (leaf rows only — never outer layout tables). */
function stripOptionalRow(html: string, token: string): string {
  const key = token.replace(/^\\{\\{|\\}\\}$/g, '');
  const byMarker = new RegExp(
    '<tr[^>]*data-ms-opt-row="' + key + '"[^>]*>(?:(?!<tr)[\\\\s\\\\S])*?</tr>',
    'g',
  );
  if (html.includes('data-ms-opt-row="' + key + '"')) {
    return html.replace(byMarker, '');
  }
  const escaped = token.replace(/[.*+?^$()|[\\]\\\\]/g, '\\\\$&');
  return html.replace(
    new RegExp('<tr[^>]*>(?:(?!<tr)[\\\\s\\\\S])*?' + escaped + '(?:(?!<tr)[\\\\s\\\\S])*?</tr>', 'g'),
    '',
  );
}

export function applyEnrolmentConfirmationShell(
  shell: string,
  values: {
    schoolName: string;
    recipientName: string;
    studentName: string;
    showStudentRow: boolean;
    className: string;
    headline: string;
    classDay?: string;
    classTime?: string;
    classStartDate?: string;
    classTeacher?: string;
    location?: string;
    amountPaid: string;
    paidOn: string;
    paymentMethod: string;
    signUrl?: string;
    deadlineDate?: string;
    primaryColor: string;
    accentColor: string;
  },
): string {
  const replacements: Record<string, string> = {
    [PLACEHOLDER.schoolName]: escapeHtml(values.schoolName),
    [PLACEHOLDER.recipientName]: escapeHtml(values.recipientName),
    [PLACEHOLDER.studentName]: escapeHtml(values.studentName),
    [PLACEHOLDER.className]: escapeHtml(values.className),
    [PLACEHOLDER.headline]: escapeHtml(values.headline),
    [PLACEHOLDER.classDay]: values.classDay ? escapeHtml(values.classDay) : PLACEHOLDER.classDay,
    [PLACEHOLDER.classTime]: values.classTime ? escapeHtml(values.classTime) : PLACEHOLDER.classTime,
    [PLACEHOLDER.classStartDate]: values.classStartDate
      ? escapeHtml(values.classStartDate)
      : PLACEHOLDER.classStartDate,
    [PLACEHOLDER.classTeacher]: values.classTeacher
      ? escapeHtml(values.classTeacher)
      : PLACEHOLDER.classTeacher,
    [PLACEHOLDER.location]: values.location ? escapeHtml(values.location) : PLACEHOLDER.location,
    [PLACEHOLDER.amountPaid]: escapeHtml(values.amountPaid),
    [PLACEHOLDER.paidOn]: escapeHtml(values.paidOn),
    [PLACEHOLDER.paymentMethod]: escapeHtml(values.paymentMethod),
    [PLACEHOLDER.signUrl]: values.signUrl ? escapeHtml(values.signUrl) : PLACEHOLDER.signUrl,
    [PLACEHOLDER.deadlineDate]: values.deadlineDate
      ? escapeHtml(values.deadlineDate)
      : PLACEHOLDER.deadlineDate,
    [PLACEHOLDER.primary]: escapeHtml(values.primaryColor),
    [PLACEHOLDER.accent]: escapeHtml(values.accentColor),
  };

  let html = shell;
  for (const [token, value] of Object.entries(replacements)) {
    html = html.split(token).join(value);
  }

  if (!values.showStudentRow) html = stripOptionalRow(html, PLACEHOLDER.studentName);
  if (!values.classDay) html = stripOptionalRow(html, PLACEHOLDER.classDay);
  if (!values.classTime) html = stripOptionalRow(html, PLACEHOLDER.classTime);
  if (!values.classStartDate) html = stripOptionalRow(html, PLACEHOLDER.classStartDate);
  if (!values.classTeacher) html = stripOptionalRow(html, PLACEHOLDER.classTeacher);
  if (!values.location) html = stripOptionalRow(html, PLACEHOLDER.location);

  for (const [cssVar, hex] of [
    ['var(--email-primary)', escapeHtml(values.primaryColor)],
    ['var(--email-accent)', escapeHtml(values.accentColor)],
    ['var(--email-text)', '#1f2937'],
    ['var(--email-neutral)', '#6b7280'],
  ]) {
    html = html.split(cssVar).join(hex);
  }

  return html;
}
`;

writeFileSync(outFile, ts, 'utf8');
console.log(`Wrote ${outFile}`);
