#!/usr/bin/env node
/**
 * Pre-render payment_reminder email HTML in Node (React Email works here).
 * Edge Functions substitute runtime values — no React in Deno.
 */
import { mkdirSync, writeFileSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath, pathToFileURL } from 'node:url';

const root = join(dirname(fileURLToPath(import.meta.url)), '..');
const outDir = join(root, 'supabase/functions/_shared/payment-email-shells');
const outFile = join(outDir, 'generated.ts');

const PLACEHOLDER = {
  schoolName: '{{SCHOOL_NAME}}',
  recipientName: '{{RECIPIENT_NAME}}',
  amountFormatted: '{{AMOUNT_FORMATTED}}',
  className: '{{CLASS_NAME}}',
  dueDate: '{{DUE_DATE}}',
  paymentUrl: '{{PAYMENT_URL}}',
  description: '{{DESCRIPTION}}',
  primary: '{{PRIMARY_COLOR}}',
  accent: '{{ACCENT_COLOR}}',
};

const BUILD_PRIMARY = '#a1b2c3';
const BUILD_ACCENT = '#d4e5f6';

const { EMAIL_TEMPLATE_NAMES, renderEmailTemplate } = await import(
  pathToFileURL(join(root, 'packages/shared/dist/email/render-template.js')).href,
);

async function renderShell(language) {
  const { html } = await renderEmailTemplate({
    templateName: EMAIL_TEMPLATE_NAMES.PAYMENT_REMINDER,
    language,
    schoolName: PLACEHOLDER.schoolName,
    tenantColors: {
      primary_color: BUILD_PRIMARY,
      accent_color: BUILD_ACCENT,
    },
    variables: {
      recipientName: PLACEHOLDER.recipientName,
      amountOutstandingFormatted: PLACEHOLDER.amountFormatted,
      amount: PLACEHOLDER.amountFormatted,
      amountFormatted: PLACEHOLDER.amountFormatted,
      enrolledClassName: PLACEHOLDER.className,
      className: PLACEHOLDER.className,
      description: PLACEHOLDER.description,
      dueDate: PLACEHOLDER.dueDate,
      paymentUrl: PLACEHOLDER.paymentUrl,
    },
    subject: `Payment required — ${PLACEHOLDER.className}`,
  });

  return html
    .split(BUILD_PRIMARY).join(PLACEHOLDER.primary)
    .split(BUILD_ACCENT).join(PLACEHOLDER.accent)
    .split('var(--email-primary)').join(PLACEHOLDER.primary)
    .split('var(--email-accent)').join(PLACEHOLDER.accent);
}

const shells = {
  en: await renderShell('en'),
  he: await renderShell('he'),
};

mkdirSync(outDir, { recursive: true });

const ts = `/** Generated by scripts/build-payment-email-shells.mjs — do not edit. */
export type PaymentEmailLanguage = 'en' | 'he';

const PLACEHOLDER = ${JSON.stringify(PLACEHOLDER)} as const;

export const PAYMENT_REMINDER_SHELLS: Record<PaymentEmailLanguage, string> = ${JSON.stringify(shells, null, 2)} as const;

function escapeHtml(value: string): string {
  return value
    .replace(/&/g, '&amp;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;')
    .replace(/"/g, '&quot;');
}

export function applyPaymentReminderShell(
  shell: string,
  values: {
    schoolName: string;
    recipientName: string;
    amountFormatted: string;
    className: string;
    dueDate: string;
    paymentUrl: string;
    description: string;
    primaryColor: string;
    accentColor: string;
  },
): string {
  const replacements: Record<string, string> = {
    [PLACEHOLDER.schoolName]: escapeHtml(values.schoolName),
    [PLACEHOLDER.recipientName]: escapeHtml(values.recipientName),
    [PLACEHOLDER.amountFormatted]: escapeHtml(values.amountFormatted),
    [PLACEHOLDER.className]: escapeHtml(values.className),
    [PLACEHOLDER.dueDate]: escapeHtml(values.dueDate),
    [PLACEHOLDER.paymentUrl]: escapeHtml(values.paymentUrl),
    [PLACEHOLDER.description]: escapeHtml(values.description),
    [PLACEHOLDER.primary]: escapeHtml(values.primaryColor),
    [PLACEHOLDER.accent]: escapeHtml(values.accentColor),
  };

  let html = shell;
  for (const [token, value] of Object.entries(replacements)) {
    html = html.split(token).join(value);
  }

  for (const [cssVar, hex] of [
    ['var(--email-primary)', escapeHtml(values.primaryColor)],
    ['var(--email-accent)', escapeHtml(values.accentColor)],
    ['var(--email-text)', '#1f2937'],
    ['var(--email-neutral)', '#6b7280'],
  ]) {
    html = html.split(cssVar).join(hex);
  }

  return html;
}
`;

writeFileSync(outFile, ts, 'utf8');
console.log(`Wrote ${outFile}`);
