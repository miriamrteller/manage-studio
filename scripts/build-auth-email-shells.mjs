#!/usr/bin/env node
/**
 * Pre-render auth email HTML in Node (React Email works here).
 * Edge Functions load the shells and substitute runtime values — no React in Deno.
 */
import { mkdirSync, writeFileSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath, pathToFileURL } from 'node:url';

const root = join(dirname(fileURLToPath(import.meta.url)), '..');
const outDir = join(root, 'supabase/functions/_shared/auth-email-shells');
const outFile = join(outDir, 'generated.ts');

const PLACEHOLDER = {
  magicLinkUrl: '{{MAGIC_LINK_URL}}',
  schoolName: '{{SCHOOL_NAME}}',
  otpCode: '{{OTP_CODE}}',
  primary: '{{PRIMARY_COLOR}}',
  accent: '{{ACCENT_COLOR}}',
};

/** Valid hex used only while rendering; replaced in output HTML. */
const BUILD_PRIMARY = '#a1b2c3';
const BUILD_ACCENT = '#d4e5f6';

/** Sentinel OTP replaced at runtime (compact display, no letter-spacing). */
const BUILD_OTP = '123456';

const { EMAIL_TEMPLATE_NAMES, renderEmailTemplate } = await import(
  pathToFileURL(join(root, 'packages/shared/dist/email/render-template.js')).href,
);

async function renderShell(language, withOtp) {
  const variables = {
    magicLinkUrl: PLACEHOLDER.magicLinkUrl,
    confirmationUrl: PLACEHOLDER.magicLinkUrl,
    expiresInMinutes: 15,
  };
  if (withOtp) {
    variables.otpCode = BUILD_OTP;
    variables.token = BUILD_OTP;
  }

  const { html } = await renderEmailTemplate({
    templateName: EMAIL_TEMPLATE_NAMES.MAGIC_LINK,
    language,
    schoolName: PLACEHOLDER.schoolName,
    tenantColors: {
      primary_color: BUILD_PRIMARY,
      accent_color: BUILD_ACCENT,
    },
    variables,
  });

  return html
    .split(BUILD_PRIMARY).join(PLACEHOLDER.primary)
    .split(BUILD_ACCENT).join(PLACEHOLDER.accent)
    .split('var(--email-primary)').join(PLACEHOLDER.primary)
    .split('var(--email-accent)').join(PLACEHOLDER.accent);
}

const shells = { en: {}, he: {} };
for (const language of ['en', 'he']) {
  shells[language].linkOnly = await renderShell(language, false);
  shells[language].withOtp = await renderShell(language, true);
}

mkdirSync(outDir, { recursive: true });

const ts = `/** Generated by scripts/build-auth-email-shells.mjs — do not edit. */
export type AuthEmailLanguage = 'en' | 'he';

const PLACEHOLDER = ${JSON.stringify(PLACEHOLDER)} as const;

const BUILD_OTP = '${BUILD_OTP}';

export const MAGIC_LINK_SHELLS: Record<
  AuthEmailLanguage,
  { linkOnly: string; withOtp: string }
> = ${JSON.stringify(shells, null, 2)} as const;

function escapeHtml(value: string): string {
  return value
    .replace(/&/g, '&amp;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;')
    .replace(/"/g, '&quot;');
}

export function applyMagicLinkShell(
  shell: string,
  values: {
    magicLinkUrl: string;
    schoolName: string;
    otpCode?: string;
    primaryColor: string;
    accentColor: string;
  },
): string {
  let html = shell
    .split(PLACEHOLDER.magicLinkUrl).join(escapeHtml(values.magicLinkUrl))
    .split(PLACEHOLDER.schoolName).join(escapeHtml(values.schoolName))
    .split(PLACEHOLDER.primary).join(escapeHtml(values.primaryColor))
    .split(PLACEHOLDER.accent).join(escapeHtml(values.accentColor));

  if (values.otpCode) {
    const digits = escapeHtml(values.otpCode.replace(/\\s/g, ''));
    html = html.split(BUILD_OTP).join(digits);
  }

  for (const [cssVar, hex] of [
    ['var(--email-primary)', escapeHtml(values.primaryColor)],
    ['var(--email-accent)', escapeHtml(values.accentColor)],
    ['var(--email-text)', '#1f2937'],
    ['var(--email-neutral)', '#6b7280'],
  ]) {
    html = html.split(cssVar).join(hex);
  }

  return html;
}
`;

writeFileSync(outFile, ts, 'utf8');
console.log(`Wrote ${outFile}`);
