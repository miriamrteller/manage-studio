CREATE OR REPLACE FUNCTION public.handle_new_user_v2()
RETURNS TRIGGER LANGUAGE plpgsql SECURITY DEFINER SET search_path = public AS $$
DECLARE
  v_tenant_id UUID;
  v_subdomain TEXT;
  v_email     TEXT;
BEGIN
  v_email     := NEW.email;
  v_subdomain := split_part(v_email, '@', 2);
  v_subdomain := split_part(v_subdomain, '.', 1);
  SELECT id INTO v_tenant_id FROM tenants WHERE subdomain = v_subdomain LIMIT 1;
  INSERT INTO public.user_profiles (id, tenant_id, role, email, created_at, updated_at)
  VALUES (NEW.id, v_tenant_id, ARRAY['account_holder']::TEXT[], v_email, NOW(), NOW())
  ON CONFLICT (id) DO UPDATE SET email = EXCLUDED.email, updated_at = NOW();
  RETURN NEW;
END;
$$;

CREATE OR REPLACE FUNCTION provision_tenant_v2(
  p_name TEXT, p_subdomain TEXT,
  p_plan               tenant_plan     DEFAULT 'essential',
  p_vertical           tenant_vertical DEFAULT 'generic',
  p_owner_email        TEXT            DEFAULT NULL,
  p_language_default   TEXT            DEFAULT 'he',
  p_country            TEXT            DEFAULT 'IL',
  p_currency           TEXT            DEFAULT 'ILS',
  p_vat_rate           NUMERIC         DEFAULT 0.18,
  p_prices_include_vat BOOLEAN         DEFAULT true,
  p_phone_region       TEXT            DEFAULT 'IL',
  p_from_email         TEXT            DEFAULT NULL,
  p_payment_provider   TEXT            DEFAULT 'grow',
  p_invoicing_provider TEXT            DEFAULT 'grow',
  p_primary_color      TEXT            DEFAULT '#6366F1',
  p_accent_color       TEXT            DEFAULT '#8B5CF6'
)
RETURNS UUID LANGUAGE plpgsql SECURITY DEFINER AS $$
DECLARE
  v_tenant_id UUID;
  v_skin      tenant_skin;
BEGIN
  v_skin := CASE p_vertical WHEN 'dance-studio' THEN 'professional'::tenant_skin ELSE 'essential'::tenant_skin END;
  INSERT INTO tenants (name, subdomain, plan, skin, vertical, sub_status, trial_ends_at,
    language_default, country, currency, vat_rate, prices_include_vat,
    phone_region, from_email, payment_provider, invoicing_provider, primary_color, accent_color)
  VALUES (p_name, p_subdomain, p_plan, v_skin, p_vertical, 'trialing', NOW() + INTERVAL '14 days',
    p_language_default, p_country, p_currency, p_vat_rate, p_prices_include_vat,
    p_phone_region, p_from_email, p_payment_provider, p_invoicing_provider, p_primary_color, p_accent_color)
  RETURNING id INTO v_tenant_id;

  IF p_vertical = 'photographer' THEN
    INSERT INTO tenant_feature_overrides (tenant_id, feature_key, enabled, granted_by, notes)
    VALUES (v_tenant_id, 'media:gallery.gated', true, 'system', 'Photographer vertical default');
  END IF;

  INSERT INTO expense_categories (tenant_id, name, description, is_vat_eligible, sort_order)
  SELECT v_tenant_id, c.name, c.description, c.is_vat_eligible, c.sort_order
  FROM (VALUES
    ('שכירות סטודיו','Studio rent',true,1), ('שכר מורים','Teacher wages',false,2),
    ('ציוד','Equipment and supplies',true,3), ('שיווק','Marketing and advertising',true,4),
    ('תוכנה ומנויים','Software subscriptions',true,5), ('ביטוח','Insurance',true,6),
    ('חשמל ומים','Utilities',true,7), ('שירותים מקצועיים','Accountant, lawyer, consultant',true,8),
    ('אחר','Other',true,9)
  ) AS c(name, description, is_vat_eligible, sort_order)
  ON CONFLICT (tenant_id, name) DO NOTHING;

  RETURN v_tenant_id;
END;
$$;

CREATE OR REPLACE FUNCTION get_tenant_config_by_subdomain_v2(p_subdomain TEXT)
RETURNS TABLE (
  id UUID, name TEXT, subdomain TEXT, business_preset TEXT,
  plan tenant_plan, skin tenant_skin, vertical tenant_vertical,
  sub_status subscription_status, trial_ends_at TIMESTAMPTZ, enabled_features TEXT[]
)
LANGUAGE plpgsql STABLE SECURITY DEFINER AS $$
BEGIN
  RETURN QUERY
  SELECT t.id, t.name, t.subdomain, t.business_preset,
         t.plan, t.skin, t.vertical, t.sub_status, t.trial_ends_at,
         get_tenant_features(t.id) AS enabled_features
  FROM tenants t WHERE t.subdomain = p_subdomain;
END;
$$;


